                    <h2 class="text-success">Decoding Baby's Cries</h2>
                    <p>Four years ago, just after the birth of our oldest daughter, I read somewhere that most mothers are able to tell what their babies want by the sound of their cries. I was awed -- and confounded -- as Phoebe's cries all sounded the same to me.</p>
                    <img
                      src="img/blog/article02.jpg"
                      alt=""
                      class="img-fluid rounded-lg shadow-sm my-4"
                    />
                    <p>Turns out I wasn't alone. In studies analyzing the acoustics of crying, even pediatric nurses could not determine the cause of a cry based on how it sounded. "It's a myth that you can tell what's wrong by the sound of the cry," says Harvey Karp, MD, a pediatrician in Los Angeles and author of The Happiest Baby on the Block. "Babies are like smoke alarms: You can't tell if you burnt the toast or if the whole house is burning down." Let’s look at some baby cry sounds and what they mean.</p>
                    
                    <h5>Is Baby Hungry?</h5>
                    <p><em>Sounds like:</em> Fairly desperate and unrelenting; usually high pitched.</p>
                    <p><em>Other clues:</em> She was breastfed more than an hour and a half to three hours ago or had a bottle two to four hours earlier. Baby roots around with mouth, wiggles, or gets frantic.</p>
                    <p><em>Solution:</em> When in doubt, assume your baby is crying because he's hungry and offer him the breast or a bottle. But if baby is bottle fed, baby may not be ready for more formula, says Deana Andersen-Tennant, a postpartum doula in Portland, Oregon, "If baby hasn't had at least two hours to digest the formula, giving more formula may cause unhappiness and discomfort."
                    </p><p>For breastfed babies, sometimes being held, but not fed, by Mom can be upsetting: Your baby can smell your milk. In this case, let Dad step in and hold the baby for a while. </p>
                    
                    <h5>Is Baby Tired?</h5>
                    <p><em>Sounds like:</em> Breathy, helpless. This cry can be intermittent and is more easily soothed than most others.</p>
                    <p><em>Other clues:</em> Baby's eyes are closed but baby is restless. Or, her eyes may be open and glassy, with redness or puffiness underneath them. Baby may rub her eyes.</p>
                    <p><em>Solution:</em> To help your baby sleep better at night, try swaddling her. Deana Andersen-Tennant, a postpartum doula in Portland, Oregon, has found that nine out of 10 babies are comforted by swaddling. (She recommends waffle-weave blankets to all her clients.) "Swaddling usually works like a charm with Whit when he's overtired," testifies Stacy Whitman, a writer in Hailey, Idaho, whose son is 9 months old. "The key is to swaddle nice and tight. I put the binky in first -- it helps him calm down so he doesn't 'fight' the swaddle."

                    </p><p>Also, if you are fairly convinced your baby is tired yet seems restless in your arms, put him down. "An overly tired baby sometimes just wants to be put down and allowed to sleep," says Andersen-Tennant.</p>
                    
                    <h5>Is Baby Bored or Overstimulated?</h5>
                    <p><em>Sounds like:</em> Usually not as loud as other cries and is often staccato. Boredom can easily transition to laughter; overstimulated can escalate to shrieking.</p>
                    <p><em>Other clues:</em> An overstimulated baby might turn his head away from you or other stimuli. He may angrily bat at an object.</p>
                    <p><em>Solution:</em> In the case of a bored baby, delay your response by a few seconds or a minute, recommends Marc Weissbluth, MD, a renowned in Chicago and author of Healthy Sleep Habits, Happy Child, who also wrote Your Fussy Baby. "In the first several weeks of life, you can't spoil your child or give him too much attention. But that doesn't mean you must respond promptly to every sound your child makes." During the delay, your baby may become engaged by the pattern on his crib bumpers or his hands in front of his face, thus eliminating the need for attention. Remind yourself you're not being cruel or unsympathetic, just laying the foundation for self-soothing.</p><p>To calm an amped-up baby, try white noise -- such as a noise machine, vacuum cleaner, or whirring fan. Carrie Barbaro Kane, of Evanston, Illinois, devised a different calming strategy: She runs warm water over the hands and feet of her 14-month-old son, Jameson.</p>
                    
                    
