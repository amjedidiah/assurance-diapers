                    <h2 class="text-success text-capitalize">3 things every new mom should know</h2>
                    <p>
                      Becoming a mom for the first time is one of those rare, blessed events that will forever change you—but just because it’s bound to be one of the greatest moments in your life doesn’t mean it’s going to be the easiest.
                    </p>
                    <img
                      src="img/blog/article01.jpg"
                      alt=""
                      class="img-fluid rounded-lg shadow-sm my-4"
                    />
                    <h5>Things may not go according to plan</h5>
                    <p>This can be a pretty soul-crushing realization for new moms. You spend all this time preparing, but as soon as your little one arrives everything goes right out the window. For me, I have a tendency to be a little obsessive. When my daughter was born, I equipped my husband with her recommended weekly feeding and sleeping schedule but my daughter had her own plans and ideas of what life would be like for her and so we had to align to her. Remember that just because you plan for your delivery to go a certain way, doesn’t necessarily guarantee it will. I had one obstetrician share with me that more than 80% of deliveries do not align with the intended birth plan.</p>
                    <h5>Bonding with Baby May Take Time</h5>
                    <p>We’ve all heard the stories about how the moment a child is born there is this heavenly euphoria that washes over the new mother…but that’s not always the case. Many new parents won’t feel that instant, heart-exploding connection for days, weeks and sometimes even months, and listen closely: THAT’S OK! When your five-week-old has spent the last four hours screaming at the top of his or her little lungs for no apparent reason, you might find yourself cursing that fresh-face new mama on Instagram who posted a gushing selfie about how much she’s enjoying every moment of motherhood (she probably had the audacity to have brushed her hair and put on lip gloss too). And the guilt you feel might even cause you to question “what’s wrong with me?” The answer? Nothing. That overwhelming, tear-inducing, can’t-live-without-you-love for your little person will come, I promise, but don’t panic if it’s not there right away. You’re in good company, I swear.</p>
                    <h5>Breastfeeding is Hard</h5>
                    <p>You’re going to hear a lot about the importance of breastfeeding—and you maybe be surprised at how passionate some people can be. While I had always planned to breastfeed my daughter, I wasn’t one of those diehard advocates. If it didn’t work for us, we’d switch to formula. My plan was to breastfeed for six months, and, when possible, begin to pump and freeze enough so that she’d be on breast milk her first year. I took the classes, bought the gear, and even invested in a deep freezer for storage. After two disastrous bouts of mastitis (an infection in the breast tissue) and weeks of wincing through feedings, my supply was next to depleted. The more stressed I became, the less I was able to produce. After two and a half months, I was done and absolutely devastated—and the deep freezer ended up being used for frozen meals (refer back to #1).
                      No one knows your journey, so no one should be passing judgment. If breastfeeding isn’t the best option for your family, that’s totally okay. You can still be a wonderful parent and have a happy, healthy baby if you choose to formula feed. And if you do decide to breastfeed, that’s great too and a wonderful way to bond with baby, but go easy on yourself. It’s often a bumpy start and for some moms can take real work. In the end, just do what you feel is best—and don’t let anyone make you feel bad about it.</p>
