                    <h2 class="text-success">How do you balance work and a new baby?</h2>
                    <p>The first thing I will say is that you are definitely not alone! Practically every working parent feels this push-pull between career and family life. Some people might make it seem easy, but it’s not. It really does take a village, and every working parent I know has the help of a daycare provider, a nanny, the grandparents, or a spouse who is the primary caregiver. You’re not alone.</p>
                    <img
                      src="img/blog/article03.jpg"
                      alt=""
                      class="img-fluid rounded-lg shadow-sm my-4"
                    />
                    <p>My second piece of advice is to remove mommy guilt from the equation. Maybe you’ll find that as much as you love your little one, you actually enjoy some time spent with other grownups while you are at work. There is absolutely nothing wrong with that. Or maybe you’ve been career-driven your entire life, but once your baby is born you decide to quit your job to become a stay-at-home mom (if your finances allow it). That is 100 percent OK too. </p>
                    <p>As far as little tips and tricks go, it really helped to getting my baby sleeping through the night and on a really good schedule. That way our days feel predictable (for the most part). Even on days I am home with my baby, I know I will always have some time to get work done during naps and after bedtime. We sleep trained early on and it was a great decision for our family.</p>
